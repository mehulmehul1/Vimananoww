// ─── Neural pulse positions (along contour paths) ────────────────────────

function generatePulsePositions(count: number): Array<{ x: number; y: number; phase: number }> {
  const positions: Array<{ x: number; y: number; phase: number }> = [];
  
  // Distribute pulses along known brain regions
  const regions = [
    { cx: 100, cy: 50, rx: 60, ry: 30 },   // frontal
    { cx: 100, cy: 98, rx: 70, ry: 40 },   // central
    { cx: 80, cy: 130, rx: 40, ry: 25 },   // temporal left
    { cx: 140, cy: 110, rx: 35, ry: 30 },  // temporal right
    { cx: 100, cy: 160, rx: 30, ry: 20 },  // occipital
  ];
  
  for (let i = 0; i < count; i++) {
    const region = regions[i % regions.length];
    const angle = (i / count) * Math.PI * 2;
    const r = 0.3 + Math.random() * 0.7;
    
    positions.push({
      x: region.cx + Math.cos(angle) * region.rx * r,
      y: region.cy + Math.sin(angle) * region.ry * r,
      phase: i / count,
    });
  }
  
  return positions;
}

