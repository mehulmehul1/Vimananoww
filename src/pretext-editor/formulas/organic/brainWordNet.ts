/** Approximate path length for SVG path string (cubic bezier sampling) */
export function approxPathLength(d: string): number {
  let total = 0;
  const cmds = d.match(/[MLCSQTAHVZ][^MLCSQTAHVZ]*/gi) || [];
  let cx = 0, cy = 0;
  for (const seg of cmds) {
    const cmd = seg.charAt(0);
    const nums = seg.slice(1).trim().split(/[\s,]+/).map(Number).filter(n => !isNaN(n));
    if (cmd === 'M') { cx = nums[0]; cy = nums[1]; }
    else if (cmd === 'L') {
      for (let i = 0; i < nums.length; i += 2) {
        total += Math.hypot(nums[i] - cx, nums[i + 1] - cy);
        cx = nums[i]; cy = nums[i + 1];
      }
    }
    else if (cmd === 'C') {
      for (let i = 0; i < nums.length; i += 6) {
        for (let t = 1; t <= 10; t++) {
          const tt = t / 10;
          const px = Math.pow(1 - tt, 3) * cx + 3 * Math.pow(1 - tt, 2) * tt * nums[i] + 3 * (1 - tt) * tt * tt * nums[i + 2] + tt * tt * tt * nums[i + 4];
          const py = Math.pow(1 - tt, 3) * cy + 3 * Math.pow(1 - tt, 2) * tt * nums[i + 1] + 3 * (1 - tt) * tt * tt * nums[i + 3] + tt * tt * tt * nums[i + 5];
          total += Math.hypot(px - cx, py - cy);
          cx = px; cy = py;
        }
      }
    }
    else if (cmd === 'S') {
      for (let i = 0; i < nums.length; i += 4) {
        for (let t = 1; t <= 10; t++) {
          const tt = t / 10;
          const px = Math.pow(1 - tt, 3) * cx + 3 * Math.pow(1 - tt, 2) * tt * nums[i] + 3 * (1 - tt) * tt * tt * nums[i + 2] + tt * tt * tt * nums[i + 2];
          const py = Math.pow(1 - tt, 3) * cy + 3 * Math.pow(1 - tt, 2) * tt * nums[i + 1] + 3 * (1 - tt) * tt * tt * nums[i + 3] + tt * tt * tt * nums[i + 3];
          total += Math.hypot(px - cx, py - cy);
          cx = px; cy = py;
        }
      }
    }
    else if (cmd === 'Z' || cmd === 'z') { /* closed */ }
  }
  return total || 60;
}

/** Pre-compute Path2D objects and lengths (once) */
export interface CompiledPath {
  path2d: Path2D;
  length: number;
}

let _compiledPaths: CompiledPath[] | null = null;

export function getCompiledPaths(): CompiledPath[] {
  if (_compiledPaths) return _compiledPaths;
  _compiledPaths = BRAIN_PATHS.map(d => ({
    path2d: new Path2D(d),
    length: approxPathLength(d),
  }));
  return _compiledPaths;
}

/** Color by language */
export function wordColor(lang: 'hi' | 'ur' | 'en', alpha: number): string {
  const a = Math.max(0, Math.min(1, alpha));
  if (lang === 'hi') return `rgba(255, 180, 80, ${a})`;   // saffron
  if (lang === 'ur') return `rgba(100, 220, 170, ${a})`;   // teal
  return `rgba(180, 190, 255, ${a})`;                       // cool blue
}
